class Color:
    def __init__(self, r=None, g=None, b=None):
        self.__r = r
        self.__g = g
        self.__b = b

    # getters and setters

    def get_r(self):
        return self.__r

    def get_g(self):
        return self.__g

    def get_b(self):
        return self.__b

    def set_r(self, color):
        __r = color

    def set_g(self, color):
        __g = color

    def set_b(self, color):
        __b = color
